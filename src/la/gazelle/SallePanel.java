     private int getComboSearchIndex(String item)
      {
          for( int i=1;i<salletyps.size() ; i++)
          {
              if(item.equals(salletyps.get(i)))
              {
                  return i;
              }
          }
          return 0;
      }
     
      private void displayTosalleTextField(int row)
    {
       // for(int i=0; combo_roomType.)
        num_s.setText(SalleTab.getModel().getValueAt(row, 0)+"");
        int indxType = getComboSearchIndex(SalleTab.getModel().getValueAt(row, 1)+"");  
        salleType.setSelectedIndex(indxType);
        prix_s.setText(SalleTab.getModel().getValueAt(row, 3)+"");
        
       
    }
