    public void listed(){
            DefaultTableModel table=new DefaultTableModel();
            
            table.addColumn("num_s");
            table.addColumn("type_s");
            table.addColumn("disponibilité");
            table.addColumn("prix");
           
            try{
             Connection konn=LaGazelle.connectTODB();
             String sql="SELECT * FROM `salle`";
             Statement S=konn.createStatement();
             ResultSet R=S.executeQuery(sql);
             
             while(R.next()){
                 table.addRow(new Object[]{
                     R.getString(1),
                     R.getString(2),
                     R.getString(3),
                     R.getString(4),
                 });
             }SalleTab.setModel(table);
            }catch(Exception e){
                System.out.println("hnaa");
            }
            
        }
    
